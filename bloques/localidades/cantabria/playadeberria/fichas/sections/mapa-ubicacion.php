    <h2>Cómo Llegar</h2>
    <p>La Playa de Berria es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Santoña, la Playa de Berria se encuentra a aproximadamente 10 minutos en coche por la carretera CA-141. Hay varias opciones de aparcamiento cerca de la playa, incluyendo zonas gratuitas.</li>
        <li><strong>En autobús:</strong> La playa está conectada mediante autobuses desde Santoña y otras localidades cercanas. Consulta los horarios y rutas de la empresa de autobuses ALSA.</li>
        <li><strong>A pie o en bicicleta:</strong> Berria y sus alrededores cuentan con caminos escénicos y carriles bici que facilitan el acceso a la playa. Es una opción ideal para disfrutar del paisaje costero y el aire libre.</li>
    </ul>
