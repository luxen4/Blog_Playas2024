    <ul>
        <li><strong>En coche:</strong> Desde Santander, la Playa de Isla se encuentra a aproximadamente 30 minutos en coche por la carretera CA-141. Hay varias opciones de aparcamiento cerca de la playa, tanto gratuitas como de pago.</li>
        <li><strong>En autobús:</strong> La playa está bien conectada mediante autobuses desde Santander y otras localidades de Cantabria. Puedes consultar los horarios y rutas de la empresa de autobuses ALSA.</li>
        <li><strong>A pie o en bicicleta:</strong> Isla y sus alrededores cuentan con caminos escénicos que permiten llegar a la playa caminando o en bicicleta, ideales para disfrutar del paisaje costero y natural.</li>
