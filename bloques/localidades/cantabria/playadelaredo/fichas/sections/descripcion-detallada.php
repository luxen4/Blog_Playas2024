    <h2>Descripción General</h2>
    <p>
        La Playa de Laredo, conocida como "La Salvé", es una de las playas más extensas de la costa cantábrica, con más de 4 kilómetros de arena dorada y fina. Ubicada en Cantabria, España, es un destino ideal para disfrutar del sol, el mar y diversas actividades recreativas en un entorno natural espectacular.
    </p>

    <h2 class="section-header text-center">Entorno Natural</h2>
    <div class="info-box">
        <p class="section-content">La Playa de Laredo está rodeada de un entorno natural impresionante que incluye acantilados y áreas verdes protegidas. Las rutas de senderismo en los alrededores permiten explorar el paisaje costero y disfrutar de vistas panorámicas del mar. La playa está cerca del Parque Natural de las Marismas de Santoña, Victoria y Joyel, lo que añade un valor ecológico significativo y ofrece una experiencia enriquecedora para los amantes de la naturaleza y los visitantes en busca de belleza natural.</p>
    </div>

    <h2 class="section-header text-center">Gastronomía</h2>
    <div class="info-box">
        <p>Laredo es un destino excelente no solo para disfrutar del mar, sino también para los amantes de la buena gastronomía. En el paseo marítimo y en el centro del pueblo, encontrarás una amplia variedad de restaurantes y bares que ofrecen desde platos tradicionales cántabros hasta opciones más modernas. No te pierdas los pescados y mariscos frescos, así como los pintxos, que son una especialidad local.</p>
