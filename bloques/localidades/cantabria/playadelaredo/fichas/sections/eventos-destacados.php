    <h2>Eventos Destacados</h2>
    <div class="events">
        <p>Laredo organiza una variedad de eventos a lo largo del año que atraen tanto a locales como a turistas:</p>
        <ul>
            <li><span class="highlight">Regata de Laredo:</span> Este evento, que se celebra en julio, es una de las regatas más importantes del Cantábrico, atrayendo a equipos de vela de toda la región.</li>
            <li><span class="highlight">Fiestas de Nuestra Señora del Carmen:</span> En julio, Laredo celebra estas fiestas con procesiones marinas, música y actividades populares.</li>
            <li><span class="highlight">Festival Internacional de Folk:</span> Durante el verano, el festival reúne a grupos de música folk de diferentes partes del mundo para actuaciones y talleres.</li>
        </ul>
    </div>

    <h2>Eventos y Festividades</h2>
    <div class="info-box">
        <p>La Playa de Laredo y el municipio en general ofrecen una rica agenda de eventos durante todo el año. Durante el verano, la playa se convierte en el escenario de eventos deportivos, conciertos y festivales. La Regata de Laredo, una competencia destacada en el ámbito de la vela, atrae a numerosos participantes y espectadores. Además, las Fiestas de Nuestra Señora del Carmen en julio llenan las calles y la playa de vida, con celebraciones que incluyen procesiones y actividades festivas. El Festival Internacional de Folk también es un evento destacado, ofreciendo música y cultura de todo el mundo.</p>
    </div>
