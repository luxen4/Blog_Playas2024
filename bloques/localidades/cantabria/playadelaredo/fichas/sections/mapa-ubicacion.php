    <h2>Cómo Llegar</h2>
    <p>La Playa de Laredo es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Santander, la Playa de Laredo se encuentra a aproximadamente 30 minutos en coche por la carretera CA-141. Hay varias opciones de aparcamiento cerca de la playa, incluyendo zonas gratuitas y de pago.</li>
        <li><strong>En autobús:</strong> Laredo está bien conectado mediante autobuses desde Santander y otras localidades cercanas. Consulta los horarios y rutas de la empresa de autobuses ALSA.</li>
        <li><strong>A pie o en bicicleta:</strong> Laredo y sus alrededores cuentan con caminos escénicos y carriles bici que facilitan el acceso a la playa. Es una opción ideal para disfrutar del paisaje costero y el aire libre.</li>
    </ul>
