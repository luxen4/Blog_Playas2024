    <h2>Cómo Llegar</h2>
    <p>La Playa de Noja es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Santander, la Playa de Noja se encuentra a unos 40 minutos en coche por la autovía A-8 y la carretera CA-147. Hay varias zonas de aparcamiento cerca de la playa, tanto gratuitas como de pago.</li>
        <li><strong>En autobús:</strong> La playa está bien conectada mediante líneas de autobús que operan desde Santander y otras localidades cercanas, con paradas a poca distancia de la playa.</li>
        <li><strong>A pie o en bicicleta:</strong> Noja cuenta con senderos y carriles bici que permiten disfrutar del paisaje mientras se llega a la playa, ofreciendo una alternativa ecológica y saludable para desplazarse.</li>
    </ul>
