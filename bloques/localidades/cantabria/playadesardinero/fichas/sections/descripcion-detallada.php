    <h2>Descripción General</h2>
    <p>
        La Playa del Sardinero, una de las playas más famosas de Santander, se extiende por aproximadamente 1.5 kilómetros. Ubicada en la costa de Cantabria, España, es un lugar perfecto para disfrutar del sol, nadar en sus aguas tranquilas y practicar deportes acuáticos en un entorno natural impresionante.
    </p>

    <h2 class="section-header text-center">Entorno Natural</h2>
    <div class="info-box">
        <p class="section-content">La Playa del Sardinero está rodeada de un entorno natural espectacular que incluye acantilados y zonas verdes como el Parque de Mataleñas. Las rutas de senderismo en los alrededores permiten explorar el paisaje costero y disfrutar de vistas panorámicas del mar. La proximidad al Parque Natural de las Dunas de Liencres ofrece un valor ecológico adicional, proporcionando una experiencia enriquecedora para los amantes de la naturaleza y los visitantes en busca de belleza natural.</p>
    </div>

    <h2 class="section-header text-center">Gastronomía</h2>
    <div class="info-box">
        <p>Santander, y en particular el Sardinero, es un excelente destino para los amantes de la gastronomía. A lo largo del paseo marítimo y en el centro de la ciudad, encontrarás una amplia gama de restaurantes y bares que ofrecen desde platos tradicionales cántabros hasta opciones más modernas. No te pierdas los pescados y mariscos frescos, así como los famosos pintxos, que son una especialidad local.</p>
