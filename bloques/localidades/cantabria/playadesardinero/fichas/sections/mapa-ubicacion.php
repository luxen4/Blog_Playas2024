    <h2>Cómo Llegar</h2>
    <p>La Playa del Sardinero es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde el centro de Santander, la Playa del Sardinero se encuentra a unos 10 minutos en coche. Puedes llegar siguiendo las señales hacia el Sardinero y encontrarás varias opciones de aparcamiento en los alrededores, tanto gratuitas como de pago.</li>
        <li><strong>En autobús:</strong> La playa está bien conectada con varias líneas de autobús que operan en Santander. Las líneas que llegan a la playa suelen ser la 1, 2, y 3. Consulta los horarios y rutas en la página web de la empresa de transporte público de Santander.</li>
        <li><strong>A pie o en bicicleta:</strong> El Sardinero y sus alrededores cuentan con una red de caminos peatonales y carriles bici que facilitan el acceso a la playa. Es una opción ideal para disfrutar del paisaje costero y el aire libre.</li>
    </ul>
