        <h2>Servicios</h2>
        <div class="info-box">
            <ul>
                <li><span class="highlight">Socorristas:</span> Servicio de socorrismo durante la temporada alta, garantizando la seguridad de los bañistas.</li>
                <li><span class="highlight">Accesibilidad:</span> Acceso adaptado para personas con movilidad reducida, incluyendo rampas y pasarelas de madera.</li>
                <li><span class="highlight">Duchas y baños:</span> Instalaciones repartidas a lo largo de la playa para mayor comodidad.</li>
                <li><span class="highlight">Aparcamiento:</span> Amplias zonas de aparcamiento cercanas a la playa, con plazas para discapacitados.</li>
                <li><span class="highlight">Alquiler de equipos:</span> Alquiler de tablas de surf, sombrillas y hamacas disponible durante todo el año.</li>
                <li><span class="highlight">Chiringuitos:</span> Varios chiringuitos ofrecen bebidas, snacks y comidas a lo largo de la playa.</li>
            </ul>
        </div>

        <h2>Actividades</h2>
        <div class="info-box">
            <ul>
                <li><span class="highlight">Surf:</span> La Playa de Somo es famosa por sus olas consistentes, ideal para surfistas de todos los niveles.</li>
                <li><span class="highlight">Paddle Surf:</span> Actividad disponible con alquiler de equipos y clases para principiantes.</li>
                <li><span class="highlight">Senderismo:</span> Disfruta de las rutas que conectan la playa con el Parque Natural de las Dunas de Liencres.</li>
                <li><span class="highlight">Voleibol Playa:</span> Canchas disponibles para jugar al voleibol con amigos y familia.</li>
                <li><span class="highlight">Kitesurf:</span> Las condiciones de viento en Somo son perfectas para practicar kitesurf.</li>
                <li><span class="highlight">Buceo:</span> Descubre la vida marina con escuelas de buceo locales que organizan inmersiones guiadas.</li>
            </ul>
        </div>

        <h2>Gastronomía</h2>
        <div class="info-box">
            <p>La Playa de Somo no solo es un paraíso para los surfistas, sino también para los amantes de la buena comida. A lo largo de la costa, encontrarás una variedad de restaurantes que ofrecen desde platos típicos cántabros hasta cocina internacional. No te pierdas los mariscos frescos y los pintxos locales.</p>
        </div>

        <h2>Eventos y Fiestas</h2>
        <div class="info-box">
            <p>Durante el verano, Somo es un lugar vibrante con numerosos eventos y festividades. Destacan las competiciones de surf, conciertos al aire libre y las tradicionales fiestas de San Juan, con hogueras en la playa y fuegos artificiales.</p>
        </div>

        <h2>Entorno Natural</h2>
        <div class="info-box">
            <p>El entorno natural de Somo es impresionante. La playa está rodeada de dunas y áreas protegidas que son ideales para explorar. Desde la playa, se pueden disfrutar vistas espectaculares de la Bahía de Santander y las montañas circundantes.</p>
        </div>

        <h2>Cómo Llegar</h2>
        <div class="info-box">
            <p>La Playa de Somo se encuentra a unos 30 minutos en coche de Santander y es fácilmente accesible en transporte público. Hay autobuses regulares desde Santander y otras localidades cercanas. También puedes tomar un ferry desde el puerto de Santander, que te dejará directamente en Somo.</p>
