    <h2>Cómo Llegar</h2>
    <p>La Playa de Somo es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Santander, la Playa de Somo se encuentra a unos 30 minutos en coche por la carretera S-10. Hay varias zonas de aparcamiento cerca de la playa, tanto gratuitas como de pago.</li>
        <li><strong>En autobús:</strong> La playa está bien conectada mediante autobuses regulares que parten desde Santander y otras localidades cercanas, lo que facilita el acceso para quienes no tienen coche.</li>
        <li><strong>En ferry:</strong> Desde el puerto de Santander, puedes tomar un ferry que te llevará directamente a Somo, una opción pintoresca y relajante para disfrutar de la bahía de Santander.</li>
        <li><strong>A pie o en bicicleta:</strong> Somo cuenta con rutas peatonales y carriles bici que facilitan el acceso a la playa, permitiendo disfrutar del paisaje natural y costero.</li>
    </ul>
