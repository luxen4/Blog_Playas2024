    <h2 class="section-header text-center">Presentación</h2>
    <p class="section-content">La Playa de Suances, situada en la costa de Cantabria, es una de las playas más destacadas de la región. Con su extensa franja de arena dorada y sus impresionantes olas, Suances ofrece un ambiente vibrante y acogedor tanto para los amantes del surf como para quienes buscan relajarse junto al mar. La playa está rodeada de un entorno natural impresionante, con acantilados y dunas que añaden belleza al paisaje costero.</p>
    <p class="section-content">Además de disfrutar de la playa, los visitantes pueden explorar el pintoresco pueblo de Suances, conocido por su rica gastronomía, especialmente sus platos a base de mariscos y pescados frescos. El área ofrece una variedad de actividades, desde deportes acuáticos hasta senderismo, lo que convierte a la Playa de Suances en un destino ideal para una experiencia completa junto al mar.</p>
    
    <h2 class="section-header text-center">Actividades Destacadas</h2>
    <div class="info-box">
        <ul>
            <li><span class="highlight">Surf:</span> Suances es famosa por sus olas consistentes, adecuadas para surfistas de todos los niveles.</li>
            <li><span class="highlight">Paddle Surf:</span> Alquiler de tablas y clases disponibles para disfrutar del paddle surf en aguas tranquilas.</li>
            <li><span class="highlight">Senderismo:</span> Disfruta de rutas escénicas que ofrecen vistas panorámicas del litoral cántabro.</li>
            <li><span class="highlight">Voleibol Playa:</span> Canchas de voleibol disponibles para aquellos que desean jugar en la arena.</li>
            <li><span class="highlight">Kitesurf:</span> Las condiciones de viento en Suances son ideales para el kitesurf, con escuelas locales que ofrecen clases y alquiler de equipos.</li>
            <li><span class="highlight">Buceo:</span> Explora la vida marina con inmersiones guiadas organizadas por centros de buceo locales.</li>
        </ul>
    </div>

    <h2 class="section-header text-center">Gastronomía</h2>
    <div class="info-box">
        <p>La Playa de Suances cuenta con una excelente selección de restaurantes y bares que sirven deliciosos platos cántabros. Disfruta de una comida frente al mar, con especial énfasis en mariscos frescos y pescados, mientras te relajas con impresionantes vistas al océano.</p>
    </div>

    <h2 class="section-header text-center">Eventos y Fiestas</h2>
    <div class="info-box">
        <p>Durante el verano, Suances se llena de vida con una variedad de eventos y festividades. Desde competiciones de surf hasta conciertos en la playa, así como celebraciones tradicionales y ferias locales, siempre hay algo emocionante para disfrutar.</p>
    </div>

    <h2 class="section-header text-center">Entorno Natural</h2>
    <div class="info-box">
        <p>El entorno natural de Suances es impresionante, con playas extensas, dunas y un paisaje costero que invita a la exploración. Aprovecha para dar paseos por los alrededores y disfrutar de la belleza del paisaje cántabro, donde la naturaleza y el mar se combinan para ofrecer una experiencia única.</p>
    </div>
