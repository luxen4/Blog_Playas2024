    <h2>Cómo Llegar</h2>
    <div class="info-box">
        <p>La Playa de Suances es fácilmente accesible tanto en coche como en transporte público.</p>
        <p>La Playa de Suances está a unos 30 minutos en coche de Santander. Puedes llegar fácilmente en transporte público desde la ciudad, con autobuses que conectan Santander con Suances. También hay aparcamiento disponible cerca de la playa para aquellos que viajan en coche.</p>
       
        <ul>
            <li><strong>En coche:</strong> Desde Santander, la Playa de Suances se encuentra a unos 30 minutos por la carretera CA-131. Hay varias opciones de aparcamiento cerca de la playa, tanto gratuitas como de pago.</li>
            <li><strong>En autobús:</strong> Suances está bien conectado mediante autobuses desde Santander y otras localidades de Cantabria. Puedes consultar los horarios y rutas en la página de la empresa de autobuses ALSA.</li>
            <li><strong>A pie o en bicicleta:</strong> Suances y sus alrededores cuentan con caminos escénicos ideales para explorar a pie o en bicicleta, permitiendo disfrutar de la belleza del paisaje costero.</li>
        </ul>
        
