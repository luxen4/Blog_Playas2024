<h3>El Pedregal</h3>
<p>El Pedregal es una zona costera en Castro Urdiales, situada en la comunidad autónoma de Cantabria, en el norte de España. <br>
    Es conocida por su paisaje rocoso y su belleza natural.
</p>

<p>Se caracteriza por su terreno rocoso y sus impresionantes vistas al mar Cantábrico. <br>
    Es un lugar popular para paseos y actividades al aire libre, y ofrece una experiencia única de la costa cantábrica.
</p>

<p>En El Pedregal, los visitantes pueden disfrutar de diversas actividades como senderismo, fotografía de paisajes y observación de aves. <br>
    Es un lugar ideal para disfrutar de la naturaleza y el mar.
</p>


<p>El Pedregal no solo es un lugar de belleza natural, sino que también tiene un valor cultural significativo para los habitantes de Castro Urdiales. <br>
    Es un sitio que refleja la relación entre la comunidad y el entorno marino.
</p>


