<h3>Iglesia de Santa María de la Asunción</h3>

<p>La Iglesia de Santa María de la Asunción se encuentra en Castro Urdiales, una ciudad en la comunidad autónoma de Cantabria, en el norte de España.

</p>

<p>Construida en el siglo XIII, la iglesia es un magnífico ejemplo de la arquitectura gótica en España. <br>
    Es la principal iglesia de Castro Urdiales y ha sido declarada Bien de Interés Cultural.
</p>

<p>La iglesia presenta un estilo gótico con una planta de cruz latina, tres naves, un ábside y diversas capillas. Destacan sus arbotantes y sus vitrales.</p>

<p>Además de su valor arquitectónico, la iglesia tiene un importante valor histórico y cultural para la región. <br>
    Alberga varias obras de arte y es un lugar de peregrinación.
</p>

