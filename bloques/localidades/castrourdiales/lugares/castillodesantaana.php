<h3>Castillo de Santa Ana</h3>
<p>El <strong>Castillo de Santa Ana</strong> es una de las construcciones más icónicas de Castro Urdiales, una ciudad costera en la región de Cantabria, España. <br> 
Este castillo, situado en una posición estratégica, ha sido un punto de defensa importante a lo largo de la historia.
</p>


<p>
    Se encuentra en un promontorio rocoso junto al puerto de Castro Urdiales, ofreciendo vistas panorámicas del mar Cantábrico y de la ciudad. Su dirección es:
</p>
<p>Monte Santa Ana, s/n<br>
    39700 Castro Urdiales, Cantabria, España</p>

<p>Construido entre los siglos XIII y XIV, el Castillo de Santa Ana formaba parte del sistema defensivo de la ciudad. <br>
    A lo largo de los siglos, ha sido utilizado para diversos fines militares y ha sido testigo de numerosos acontecimientos históricos. <br>
    En la actualidad, el castillo ha sido restaurado y es un importante atractivo turístico.
</p>

