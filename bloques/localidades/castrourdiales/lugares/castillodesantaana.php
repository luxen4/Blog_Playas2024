<p>Se caracteriza por su robusta construcción y su estilo medieval: <br>
    <strong>Torreón:</strong> Un torreón principal que servía como punto de vigilancia y defensa. <br>
    <strong>Murallas:</strong> Fuertes murallas que rodean el castillo, proporcionando una defensa efectiva contra invasores. <br>
    <strong>Faro:</strong> En el siglo XIX, se construyó un faro sobre el castillo, que todavía está en funcionamiento y es un elemento distintivo del paisaje.
</p>

<p>El Castillo de Santa Ana está abierto al público y ofrece diversas actividades y puntos de interés:
    <strong>Visitas Guiadas:</strong> Tours que exploran la historia y la arquitectura del castillo. <br>
    <strong>Exposiciones:</strong> Exhibiciones temporales y permanentes sobre la historia local y la vida en la época medieval. <br>
    <strong>Vistas Panorámicas:</strong> Miradores que ofrecen impresionantes vistas del puerto y del mar. <br>
    <strong>Eventos Culturales:</strong> Actuaciones, recreaciones históricas y otros eventos culturales que se celebran en el castillo.
</p>

<p>Es un símbolo de la rica herencia histórica de Castro Urdiales. <br>
    Su preservación y restauración permiten a los visitantes conectar con el pasado de la región y apreciar su importancia estratégica y cultural.
</p>

