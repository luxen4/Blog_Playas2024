<h3>Club Náutico</h3>
<p>El <strong>Club Náutico</strong> de Castro Urdiales es una institución emblemática situada en esta pintoresca ciudad costera de Cantabria, España. Fundado en el siglo XX, este club es un centro de actividades marítimas y recreativas, proporcionando instalaciones y servicios de alta calidad para sus socios y visitantes.</p>

<p>Se encuentra en el puerto de Castro Urdiales, ofreciendo una ubicación privilegiada con vistas panorámicas del mar Cantábrico. Su dirección exacta es:
    Avenida de la Constitución, s/n<br>
    39700 Castro Urdiales, Cantabria, España</p>

<p>Fue fundado en el siglo XX con el objetivo de promover las actividades náuticas y recreativas en la región. <br>
    A lo largo de los años, el club ha crecido en membresía e instalaciones, convirtiéndose en un referente en la comunidad náutica local y regional.
</p>


<p>Cuenta con modernas instalaciones que incluyen: <br>
    <strong>Pantalanes y Muelles:</strong> Espacios bien equipados para el atraque de embarcaciones de diferentes tamaños. <br>
    <strong>Escuela de Vela:</strong> Programas de formación para todas las edades y niveles de habilidad.<br>
    <strong>Restaurante y Bar:</strong> Un restaurante con vistas al mar que ofrece una variada carta de platos locales e internacionales.<br>
    <strong>Salas de Reuniones:</strong> Espacios para eventos y reuniones sociales o de negocios.<br>
    <strong>Zona de Deportes:</strong> Instalaciones deportivas para la práctica de diversos deportes acuáticos y terrestres.<br>
</p>

