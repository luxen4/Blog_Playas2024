<h3>5. Puerto de Castro Urdiales</h3>

<p>El Puerto de Castro Urdiales es uno de los lugares más emblemáticos de la ciudad de Castro Urdiales, situada en la comunidad autónoma de Cantabria, en el norte de España. <br>
    Este puerto pesquero y deportivo ofrece unas vistas impresionantes del Mar Cantábrico y es un punto de encuentro para turistas y locales.
</p>

<p>El puerto tiene una larga historia que se remonta a la época romana, y hoy en día combina la tradición pesquera con modernas instalaciones para embarcaciones de recreo. <br> 
Es un lugar perfecto para pasear, disfrutar de la gastronomía local en los restaurantes cercanos y contemplar las vistas panorámicas.
</p>

<p>Hoy en día, el Puerto de Castro Urdiales es un lugar vibrante donde se pueden realizar diversas actividades como: <br>

    <strong>Paseos en barco y deportes náuticos.</strong> <br>
    <strong>Pesca deportiva.</strong> <br>
    <strong>Excursiones turísticas para explorar la costa y sus alrededores.</strong> <br>
    <strong>Visitas a monumentos históricos como la Iglesia de Santa María de la Asunción y el Castillo-Faro de Santa Ana.</strong>

</p>



