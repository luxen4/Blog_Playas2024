    <h2 class="section-header text-center">Actividades Recomendadas en la Playa de Biarritz</h2>
    <ul class="list-unstyled">
        <li><strong>Surf:</strong> Disfruta de algunas de las mejores olas del Atlántico para practicar surf en una de las playas más icónicas del País Vasco Francés.</li>
        <li><strong>Senderismo:</strong> Explora los pintorescos senderos que rodean la playa y obtén vistas impresionantes del océano y de la costa vasca francesa.</li>
        <li><strong>Gastronomía Local:</strong> Saborea la exquisita cocina vasca en los restaurantes y bistrós cercanos, conocidos por sus platos de mariscos frescos y la famosa cocina francesa.</li>
        <li><strong>Eventos Culturales:</strong> Participa en una variedad de eventos y festivales que se celebran durante todo el año, como el Festival de Surf de Biarritz y eventos culturales locales.</li>
    </ul>
