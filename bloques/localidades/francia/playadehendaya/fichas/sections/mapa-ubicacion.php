    <h2>Cómo Llegar</h2>
    <p>La Playa de Hendaya es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde San Sebastián, la Playa de Hendaya se encuentra a unos 30 minutos en coche por la carretera N-1 y la AP-8. Hay varias opciones de aparcamiento cerca de la playa, incluyendo zonas gratuitas y de pago.</li>
        <li><strong>En autobús:</strong> La playa está bien conectada mediante autobuses desde San Sebastián y otras localidades cercanas. Consulta los horarios y rutas de la empresa de autobuses local.</li>
        <li><strong>A pie o en bicicleta:</strong> Hendaya y sus alrededores cuentan con una red de rutas peatonales y carriles bici que facilitan el acceso a la playa. Es una opción ideal para disfrutar del paisaje y el aire libre.</li>
    </ul>

