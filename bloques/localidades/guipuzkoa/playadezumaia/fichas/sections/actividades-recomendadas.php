    <h2 class="section-header text-center">Actividades Recomendadas</h2>
    <p>La Playa de Zumaia ofrece una variedad de actividades para todo tipo de visitantes:</p>
    <ul class="list-unstyled">
        <li><strong>Senderismo:</strong> Recorre la famosa Ruta del Flysch, que te llevará a través de paisajes espectaculares y te permitirá observar de cerca las formaciones rocosas.</li>
        <li><strong>Fotografía:</strong> Captura la belleza única de los acantilados y las vistas panorámicas del mar Cantábrico.</li>
        <li><strong>Surf:</strong> Las olas de Zumaia son ideales para los surfistas de todos los niveles, especialmente durante la marea alta.</li>
        <li><strong>Paseos en barco:</strong> Explora la costa desde una perspectiva diferente con los paseos en barco que salen del puerto de Zumaia.</li>
        <li><strong>Visitas guiadas:</strong> Participa en una visita guiada para aprender más sobre la historia geológica de la zona.</li>
    </ul>
</section>




