    <h2>Servicios</h2>
    <div class="services">
        <ul>
            <li><strong>Socorristas:</strong> Servicio de socorristas durante la temporada de verano para garantizar la seguridad de los bañistas.</li>
            <li><strong>Duchas y baños:</strong> Duchas de agua dulce y baños públicos están disponibles para mayor comodidad.</li>
            <li><strong>Aparcamiento:</strong> Aparcamiento cercano con plazas limitadas, ideal para quienes llegan en coche.</li>
            <li><strong>Accesibilidad:</strong> Acceso adaptado para personas con movilidad reducida, con rampas y pasarelas.</li>
            <li><strong>Chiringuitos:</strong> Varios chiringuitos a lo largo de la playa donde se puede disfrutar de bebidas, snacks y platos típicos.</li>
            <li><strong>Alquiler de equipos:</strong> Posibilidad de alquilar equipos de surf, sombrillas y hamacas.</li>
        </ul>
    </div>

    <h2>Actividades</h2>
    <div class="activities">
        <ul>
            <li><strong>Surf:</strong> Las olas de Getaria son ideales tanto para principiantes como para surfistas más experimentados. Se ofrecen clases de surf para todos los niveles.</li>
            <li><strong>Natación:</strong> Las aguas tranquilas y seguras son perfectas para nadar y disfrutar en familia.</li>
            <li><strong>Senderismo:</strong> Rutas de senderismo cercanas permiten explorar los alrededores, con impresionantes vistas de la costa y el mar Cantábrico.</li>
            <li><strong>Paseos en barco:</strong> Excursiones en barco están disponibles desde el puerto de Getaria, ofreciendo una perspectiva única de la costa.</li>
            <li><strong>Pesca:</strong> Getaria es un lugar conocido por su pesca, tanto desde la playa como en alta mar.</li>
            <li><strong>Deportes acuáticos:</strong> Además del surf, se pueden practicar paddle surf y kayak.</li>
        </ul>
    </div>

    <h2>Gastronomía</h2>
    <div class="gastronomy">
        <p>La gastronomía de Getaria es famosa por sus pescados y mariscos frescos. Los restaurantes a lo largo del puerto y en el pueblo ofrecen platos tradicionales vascos como el <strong>txakoli</strong>, un vino blanco local, y el <strong>rodaballo a la brasa</strong>. No te pierdas la oportunidad de disfrutar de una auténtica comida vasca con vistas al mar.</p>
    </div>

    <h2>Eventos Destacados</h2>
    <div class="events">
        <p>Getaria acoge varios eventos a lo largo del año que atraen a visitantes de todas partes:</p>
        <ul>
            <li><strong>Fiestas de San Antón:</strong> Celebradas en enero, incluyen procesiones, danzas tradicionales y degustaciones de productos locales.</li>
            <li><strong>Concurso de Pesca:</strong> Durante el verano, se organiza un concurso de pesca en el que participan tanto locales como turistas.</li>
            <li><strong>Festival del Txakoli:</strong> Un evento anual que celebra el famoso vino blanco de la región, con catas, música y bailes.</li>
        </ul>
    </div>

    <h2>Entorno Natural</h2>
    <p>La Playa de Getaria está rodeada de un impresionante entorno natural. Al oeste, se encuentra el Monte San Antón, conocido como el "Ratón de Getaria" por su peculiar forma. Es un excelente lugar para realizar senderismo y disfrutar de vistas panorámicas del mar. Al este, los acantilados ofrecen un espectáculo visual, especialmente al atardecer.</p>

    <h2>Cómo Llegar</h2>
    <p>Getaria se encuentra a aproximadamente 25 km de San Sebastián, fácilmente accesible en coche o autobús. También es un destino popular para ciclistas que recorren la costa vasca. El pueblo de Getaria es pequeño pero encantador, conocido por ser la cuna del navegante Juan Sebastián Elcano y el modisto Cristóbal Balenciaga, cuyo museo se encuentra en la localidad.</p>
