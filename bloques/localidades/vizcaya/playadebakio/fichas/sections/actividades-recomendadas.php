    <h2 class="section-header text-center">Actividades Recomendadas</h2>
    <ul class="list-unstyled">
        <li><strong>Surf:</strong> Bakio es uno de los mejores destinos de la Costa Vasca para surfear, con olas que atraen a surfistas de todos los niveles.</li>
        <li><strong>Senderismo y naturaleza:</strong> Disfruta de rutas de senderismo por la naturaleza, como el camino hacia San Juan de Gaztelugatxe, con vistas impresionantes del litoral.</li>
        <li><strong>Gastronomía Local:</strong> Prueba los platos tradicionales de la cocina vasca, especialmente los mariscos frescos y el txakoli, un vino típico de la región.</li>
        <li><strong>Visitas culturales:</strong> Explora el pequeño pero encantador casco antiguo de Bakio y visita la ermita de San Pelayo o los viñedos de txakoli cercanos.</li>
    </ul>
</section>