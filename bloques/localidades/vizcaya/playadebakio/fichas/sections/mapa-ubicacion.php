    <h2>Cómo Llegar</h2>
    <p>La Playa de Bakio es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Bilbao, la Playa de Bakio se encuentra a unos 30 minutos en coche por la carretera BI-631. Hay varias opciones de aparcamiento cerca de la playa, incluyendo zonas gratuitas y de pago.</li>
        <li><strong>En autobús:</strong> Bakio está bien conectada mediante autobuses desde Bilbao y otras localidades cercanas. Consulta los horarios y rutas de la empresa de autobuses Bizkaibus para más detalles.</li>
        <li><strong>A pie o en bicicleta:</strong> Bakio y sus alrededores cuentan con rutas escénicas ideales para caminar o andar en bicicleta, permitiendo disfrutar del paisaje costero y el entorno natural.</li>
    </ul>
</section>