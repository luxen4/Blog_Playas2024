    <h2>Cómo Llegar</h2>
    <p>La Playa de Getxo es de fácil acceso tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Bilbao, la Playa de Getxo se encuentra a unos 20 minutos en coche por la carretera BI-637. Hay varias opciones de aparcamiento cerca de la playa, incluyendo zonas gratuitas y de pago.</li>
        <li><strong>En metro:</strong> La playa está bien conectada mediante la línea 1 del metro de Bilbao, con la estación de Getxo a solo unos minutos a pie de la playa.</li>
        <li><strong>A pie o en bicicleta:</strong> Getxo y sus alrededores cuentan con una red de rutas peatonales y carriles bici que facilitan el acceso a la playa. Es una opción ideal para disfrutar del paisaje y el aire libre.</li>
    </ul>
