        <h2>Servicios</h2>
        <div class="info-box">
            <ul>
                <li><span class="highlight">Socorristas:</span> Servicio de socorrismo disponible durante la temporada alta para garantizar la seguridad de todos los bañistas.</li>
                <li><span class="highlight">Accesibilidad:</span> La playa cuenta con acceso para personas con movilidad reducida, incluyendo rampas y sillas anfibias.</li>
                <li><span class="highlight">Duchas y baños:</span> Instalaciones de duchas de agua dulce y baños públicos repartidos a lo largo de la playa.</li>
                <li><span class="highlight">Aparcamiento:</span> Amplia zona de aparcamiento gratuita cerca de la playa, con plazas reservadas para discapacitados.</li>
                <li><span class="highlight">Alquiler de sombrillas y hamacas:</span> Servicios de alquiler disponibles durante los meses de verano.</li>
                <li><span class="highlight">Chiringuitos:</span> Varios chiringuitos a lo largo de la playa que ofrecen refrescos, helados y comidas ligeras.</li>
            </ul>
        </div>

        <h2>Actividades</h2>
        <div class="info-box">
            <ul>
                <li><span class="highlight">Surf:</span> La Playa de Gorliz es ideal para el surf, especialmente para principiantes, gracias a sus olas suaves y constantes.</li>
                <li><span class="highlight">Kayak:</span> Alquiler de kayaks disponible para explorar la costa y las aguas tranquilas de la bahía.</li>
                <li><span class="highlight">Paddle Surf:</span> Actividad popular en Gorliz, con opciones para alquilar tablas y recibir clases.</li>
                <li><span class="highlight">Senderismo:</span> Disfruta de las rutas de senderismo que parten desde la playa, incluyendo el famoso camino al Faro de Gorliz, con vistas espectaculares.</li>
                <li><span class="highlight">Buceo:</span> Las aguas cristalinas de Gorliz son perfectas para el buceo, con la posibilidad de explorar la rica vida marina de la zona.</li>
                <li><span class="highlight">Vela:</span> La bahía de Gorliz es un lugar ideal para practicar vela, con escuelas que ofrecen clases para todos los niveles.</li>
            </ul>
        </div>

        <h2>Gastronomía</h2>
        <div class="info-box">
            <p>Gorliz es un destino no solo para los amantes del mar, sino también para los aficionados a la buena mesa. A lo largo de la playa y en el cercano pueblo, encontrarás una variedad de restaurantes y bares que ofrecen desde platos tradicionales vascos hasta opciones más internacionales. No te pierdas los pescados y mariscos frescos, ni los pintxos, que son una delicia local.</p>
        </div>

        <h2>Eventos y Fiestas</h2>
        <div class="info-box">
            <p>A lo largo del año, Gorliz celebra diversas fiestas y eventos que atraen tanto a locales como a visitantes. Durante el verano, la playa es el epicentro de actividades como competiciones de surf, fiestas nocturnas en los chiringuitos y conciertos al aire libre. En junio, la localidad celebra San Juan con hogueras en la playa y fuegos artificiales, una experiencia mágica para quienes visitan en esa época.</p>
        </div>

        <h2>Entorno Natural</h2>
        <div class="info-box">
            <p>El entorno natural que rodea a la Playa de Gorliz es simplemente impresionante. Los acantilados de Armintza, el Faro de Gorliz y las cercanas marismas del estuario de Plentzia ofrecen paisajes únicos para explorar. Estas zonas son perfectas para quienes disfrutan de la fotografía, la observación de aves o simplemente pasear rodeados de naturaleza.</p>
        </div>

        <h2>Cómo Llegar</h2>
        <div class="info-box">
            <p>La Playa de Gorliz se encuentra a unos 30 km de Bilbao y es fácilmente accesible en coche, autobús o metro. Si vienes en coche, sigue la BI-3158 desde Plentzia, y encontrarás señalización que te guiará directamente a la playa. El servicio de autobuses conecta Gorliz con Bilbao y otras localidades cercanas, y la estación de metro más cercana está en Plentzia, desde donde puedes caminar o tomar un autobús local.</p>
