    <h2>Cómo Llegar</h2>
    <p>La Playa de Lekeitio es fácilmente accesible tanto en coche como en transporte público.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Bilbao, la Playa de Lekeitio se encuentra a aproximadamente una hora y cuarto por la carretera BI-633. Hay varias opciones de aparcamiento cerca del puerto y de la playa, tanto gratuitas como de pago.</li>
        <li><strong>En autobús:</strong> Lekeitio está bien conectado mediante autobuses desde Bilbao y otras localidades de Bizkaia. Puedes consultar los horarios y rutas de la empresa de autobuses Bizkaibus.</li>
        <li><strong>A pie o en bicicleta:</strong> Lekeitio y sus alrededores cuentan con caminos escénicos que permiten llegar a la playa caminando o en bicicleta, ideales para disfrutar de la naturaleza y las vistas costeras.</li>
    </ul>
</section>