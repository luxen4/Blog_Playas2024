        <h2>Cómo Llegar</h2>
        <p>La Playa de Sopelana es fácilmente accesible tanto en coche como en transporte público.</p>
        <ul>
            <li><strong>En coche:</strong> Desde Bilbao, se tarda unos 25 minutos en llegar por la carretera BI-637. Hay varios aparcamientos disponibles cerca de la playa, aunque pueden llenarse rápidamente durante los días soleados y festivos.</li>
            <li><strong>En metro:</strong> La línea 1 del metro de Bilbao tiene una parada en Sopelana. Desde allí, puedes caminar hasta la playa en aproximadamente 15 minutos o tomar un autobús local para un acceso más rápido.</li>
            <li><strong>A pie o en bicicleta:</strong> Sopelana y sus alrededores cuentan con rutas peatonales y carriles bici que conectan directamente con la playa, ofreciendo una opción agradable para aquellos que disfrutan de un paseo o un recorrido en bicicleta.</li>
        </ul>
    </section>

