    <h2>Servicios</h2>
    <p>La Playa de Sopelana está bien equipada con una variedad de servicios para los visitantes:</p>
    <ul>
        <li><strong>Socorristas:</strong> Durante la temporada alta, la playa cuenta con un equipo de socorristas que garantizan la seguridad de los bañistas.</li>
        <li><strong>Duchas y vestuarios:</strong> Hay instalaciones disponibles para ducharse y cambiarse después de un día de playa.</li>
        <li><strong>Chiringuitos:</strong> Varios bares y restaurantes ofrecen comida y bebida, desde tapas tradicionales hasta refrescantes cócteles.</li>
        <li><strong>Alquiler de equipos:</strong> Se pueden alquilar tablas de surf, neoprenos y otros equipos deportivos en locales cercanos a la playa.</li>
        <li><strong>Aparcamiento:</strong> Hay aparcamientos gratuitos y de pago cerca de la playa, aunque se recomienda llegar temprano para asegurar plaza.</li>
        <li><strong>Acceso para discapacitados:</strong> La playa cuenta con rampas y accesos adecuados para personas con movilidad reducida.</li>
    </ul>
</section>