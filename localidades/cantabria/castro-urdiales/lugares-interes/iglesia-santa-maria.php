    <h1 class="text-primary mb-4">Iglesia de Santa María de la Asunción</h1>

    <p>La <strong>Iglesia de Santa María de la Asunción</strong> se encuentra en Castro Urdiales, una ciudad en la comunidad autónoma de Cantabria, en el norte de España.</p>

    <p>Construida en el <strong>siglo XIII</strong>, esta iglesia es un magnífico ejemplo del <strong>estilo gótico</strong> en España. Es el principal templo religioso de Castro Urdiales y ha sido declarado <em>Bien de Interés Cultural</em>.</p>

    <p>La iglesia destaca por su arquitectura gótica: cuenta con planta de cruz latina, tres naves, ábside y varias capillas laterales. Llaman especialmente la atención sus <strong>arbotantes</strong>, sus <strong>vidrieras</strong> y sus detalles escultóricos en piedra.</p>

    <p>Además de su riqueza arquitectónica, la iglesia alberga <strong>obras de arte religioso</strong> y posee un importante valor histórico y cultural. Es un punto de referencia espiritual para la comunidad y recibe cada año a numerosos peregrinos y visitantes.</p>

