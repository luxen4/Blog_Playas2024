    <h1 class="text-primary mb-4">Palacio Ocharan (Toki Eder)</h1>

    <p>El <strong>Palacio Ocharan</strong>, también conocido como el <em>Palacio de Toki Eder</em>, es una de las edificaciones más emblemáticas de Castro Urdiales, una ciudad costera en Cantabria, España. Este palacio destaca por su arquitectura y su historia, siendo un punto de interés turístico importante.</p>

    <p>Se encuentra en la <strong>Avenida de la Constitución</strong>, cerca del centro de Castro Urdiales. Su ubicación privilegiada ofrece vistas impresionantes de la ciudad y del mar Cantábrico.</p>

    <p>Fue construido a finales del <strong>siglo XIX</strong> por el arquitecto <strong>Eladio Laredo</strong>. Encargado por Luis de Ocharan Mazas, un empresario e industrial, el palacio combina estilos <strong>neogótico</strong> y <strong>modernistas</strong>.</p>

    <p>Entre sus características más destacadas se encuentran:</p>
    <ul>
      <li><strong>Estilo arquitectónico:</strong> mezcla de neogótico y modernismo, con detalles ornamentales únicos.</li>
      <li><strong>Torre y murallas:</strong> una torre prominente y murallas que le dan un aire de castillo medieval.</li>
      <li><strong>Jardines:</strong> rodeado de jardines con plantas y árboles exóticos.</li>
      <li><strong>Interiores:</strong> aunque no siempre accesibles, están lujosamente decorados y conservan mobiliario de época.</li>
    </ul>

