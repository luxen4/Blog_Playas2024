    <p>Actualmente es una <strong>propiedad privada</strong> y no se puede visitar de forma regular. Sin embargo, su fachada y jardines se pueden admirar desde la calle, y forman parte del patrimonio cultural de Castro Urdiales.</p>

    <p>En <strong>ocasiones especiales</strong>, como jornadas de puertas abiertas o eventos culturales, se permite el acceso parcial al palacio y sus jardines. Es una excelente oportunidad para conocer esta joya arquitectónica por dentro.</p>

    <p>El Palacio Ocharan es un símbolo de la riqueza cultural e histórica de la ciudad. Su silueta imponente y su elegante diseño lo convierten en un <strong>referente imprescindible</strong> para quienes visitan Castro Urdiales.</p>

