    <p>El <strong>Parque Amestoy</strong> es una zona verde ubicada en Castro Urdiales, una pintoresca ciudad costera en la región de Cantabria, España. Este parque es un lugar destacado en la ciudad y ofrece un espacio de recreo y descanso tanto para los residentes locales como para los visitantes.</p>

    <p>Se encuentra en una <strong>ubicación céntrica</strong> de Castro Urdiales, cerca del puerto y del paseo marítimo. Esta situación lo hace fácilmente accesible y un lugar de paso frecuente para quienes recorren el centro de la ciudad.</p>

    <p>Cuenta con amplias zonas de césped, árboles, y bancos, lo que lo convierte en un lugar ideal para pasear, relajarse o disfrutar de un picnic. También dispone de <strong>áreas de juegos infantiles</strong>, lo que lo hace atractivo para familias con niños.</p>

    <p>A lo largo del año, el parque acoge <strong>eventos culturales y actividades al aire libre</strong>, como conciertos, ferias y mercados, consolidándose como un punto de encuentro importante para la comunidad.</p>

    <p>Está próximo a otros puntos de interés de la ciudad, como la <strong>Iglesia de Santa María de la Asunción</strong>, el <strong>Castillo-Faro de Santa Ana</strong> y las <strong>playas urbanas</strong>, permitiendo a los visitantes combinar naturaleza, cultura y ocio en un solo recorrido.</p>

