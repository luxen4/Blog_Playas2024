    <h1 class="text-primary mb-4">Playa de Ostende</h1>

    <p>La <strong>Playa de Ostende</strong> se encuentra en Castro Urdiales, una ciudad costera en la comunidad autónoma de Cantabria, en el norte de España. Es una de las playas más populares de la localidad.</p>

    <p>Es una playa extensa de arena dorada y aguas tranquilas, ideal para nadar y disfrutar del sol. Cuenta con una longitud de aproximadamente 900 metros y un ancho medio de 40 metros.</p>

    <p>La playa ofrece una amplia gama de servicios para los visitantes, incluyendo:</p>
    <ul>
      <li>Duchas y baños públicos.</li>
      <li>Accesibilidad para personas con movilidad reducida.</li>
      <li>Alquiler de sombrillas y hamacas.</li>
      <li>Chiringuitos y bares con comida y bebida junto al mar.</li>
    </ul>

    <p>En la Playa de Ostende se pueden realizar diversas actividades recreativas y deportivas como:</p>
    <ul>
      <li>Voleibol de playa.</li>
      <li>Surf y paddle surf.</li>
      <li>Paseos en barco.</li>
      <li>Buceo y snorkeling en sus aguas claras.</li>
    </ul>

    <p>Además de ser un lugar para el ocio y el disfrute, la Playa de Ostende tiene un valor cultural significativo para la comunidad local. Es un punto de encuentro para los habitantes y un destino turístico importante que contribuye a la economía de Castro Urdiales.</p>

