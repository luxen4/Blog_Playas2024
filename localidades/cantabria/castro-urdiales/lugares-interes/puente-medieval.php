    <h1 class="text-primary mb-4">Puente Medieval</h1>

    <p>El <strong>Puente Medieval</strong> de Castro Urdiales es una de las estructuras más antiguas y emblemáticas de esta hermosa ciudad costera en la región de Cantabria, España. 
    Este puente es un testimonio vivo de la historia medieval de la ciudad y es un punto de interés destacado para los visitantes.</p>

    <p>Se encuentra en el casco histórico de Castro Urdiales, cerca de la iglesia de Santa María de la Asunción y el Castillo Faro de Santa Ana. 
    Su posición estratégica ofrece vistas impresionantes del puerto y el mar Cantábrico.</p>

    <p>Fue construido en la Edad Media, y ha sido un importante punto de conexión en la ciudad durante siglos. 
    Ha sido testigo de numerosos eventos históricos y ha sobrevivido a lo largo del tiempo como un símbolo de la durabilidad y la importancia histórica de Castro Urdiales.</p>

    <p>Es conocido por su arquitectura robusta y su diseño funcional:</p>
    <ul>
      <li><strong>Construcción en Piedra:</strong> El puente está construido principalmente de piedra, lo que le confiere una apariencia sólida y duradera.</li>
      <li><strong>Arcos:</strong> Presenta un diseño de arcos que es característico de la arquitectura medieval, proporcionando estabilidad y una estética distintiva.</li>
      <li><strong>Entorno Pintoresco:</strong> El puente se integra perfectamente con el paisaje circundante, ofreciendo vistas panorámicas del puerto y la ciudad.</li>
      <li><strong>Acceso Peatonal:</strong> El puente es accesible para los peatones, lo que permite a los visitantes cruzar y disfrutar del entorno histórico y natural.</li>
    </ul>

    <p>No solo es una estructura arquitectónica impresionante, sino también un símbolo cultural importante para Castro Urdiales. 
    Representa la rica herencia histórica de la ciudad y es un recordatorio de su pasado medieval.</p>

    <p>Es un lugar popular tanto para los turistas como para los residentes locales. Algunas actividades que se pueden disfrutar en y alrededor del puente incluyen:</p>
    <ul>
      <li><strong>Paseos:</strong> Caminar por el puente y sus alrededores para disfrutar de las vistas y la historia.</li>
      <li><strong>Fotografía:</strong> El puente y su entorno ofrecen excelentes oportunidades para la fotografía, especialmente al atardecer.</li>
      <li><strong>Exploración del Casco Histórico:</strong> Visitar el puente como parte de un recorrido más amplio por el casco histórico de Castro Urdiales, incluyendo la iglesia de Santa María de la Asunción y el Castillo Faro de Santa Ana.</li>
    </ul>

    <p>El Puente Medieval de Castro Urdiales es sin duda una joya histórica y arquitectónica que ofrece una ventana al 
    pasado de la ciudad y una experiencia memorable para todos los que lo visitan.</p>

