    <h2 class="section-header text-center">Actividades Recomendadas</h2>
    <ul class="list-unstyled">
        <li><strong>Surf:</strong> Las olas de Getaria son ideales tanto para principiantes como para surfistas más experimentados. Se ofrecen clases de surf para todos los niveles.</li>
        <li><strong>Natación:</strong> Las aguas tranquilas y seguras son perfectas para nadar y disfrutar en familia.</li>
        <li><strong>Senderismo:</strong> Rutas de senderismo cercanas permiten explorar los alrededores, con impresionantes vistas de la costa y el mar Cantábrico.</li>
        <li><strong>Paseos en barco:</strong> Excursiones en barco están disponibles desde el puerto de Getaria, ofreciendo una perspectiva única de la costa.</li>
        <li><strong>Pesca:</strong> Getaria es un lugar conocido por su pesca, tanto desde la playa como en alta mar.</li>
        <li><strong>Deportes acuáticos:</strong> Además del surf, se pueden practicar paddle surf y kayak.</li>
    </ul>
