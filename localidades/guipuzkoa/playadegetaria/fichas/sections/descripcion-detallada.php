<section id="presentacion" class="container my-5">
    <h2 class="section-header text-left">Información General</h2>
    <ul class="section-content">
        <li><strong>Ubicación:</strong> La Playa de Getaria, situada en la provincia de Gipuzkoa, es un encantador destino costero en el País Vasco.</li>
        
        <li><strong>Características:</strong> 
            <ul>
                <li><u>Arena dorada y entorno natural:</u> Ofrece una franja de arena dorada y un entorno natural espectacular.</li>
                <li><u>Ambiente:</u> Relajado y pintoresco, ideal tanto para locales como para visitantes.</li>
            </ul>
        </li>
        
        <li><strong>Tradición:</strong> Getaria es conocida por su rica tradición pesquera y su hermoso puerto, lo que la convierte en un lugar ideal para disfrutar de la costa vasca.</li>
        
        <li><strong>Historia:</strong> 
            <ul>
                <li>Getaria es el lugar de nacimiento del célebre navegante <strong>Juan Sebastián Elcano</strong>.</li>
                <li>El casco antiguo está lleno de historia y ofrece un encanto especial a la localidad.</li>
            </ul>
        </li>
        
        <li><strong>Gastronomía:</strong> 
            <ul>
                <li><u>Pescados y mariscos frescos:</u> La oferta gastronómica de Getaria destaca por sus pescados y mariscos frescos.</li>
                <li>Prueba el <strong>txakoli</strong> (vino blanco local) y el <strong>rodaballo a la brasa</strong> en los restaurantes a lo largo del puerto.</li>
            </ul>
        </li>
        
        <li><strong>Actividades y Eventos:</strong> La playa y la localidad de Getaria ofrecen una variedad de actividades recreativas y eventos culturales durante todo el año:
            <ul>
                <li><strong>Fiestas de San Antón (enero):</strong> Procesiones, danzas tradicionales y degustaciones de productos locales.</li>
                <li><strong>Concurso de Pesca (verano):</strong> Un evento que atrae tanto a locales como a turistas.</li>
                <li><strong>Festival del Txakoli (anual):</strong> Catas de vino, música y bailes en celebración del famoso vino blanco de la región.</li>
            </ul>
        </li>
        
        <li><strong>Entorno Natural:</strong> 
            <ul>
                <li><u>Monte San Antón:</u> Al oeste, también conocido como el "Ratón de Getaria", es un excelente lugar para senderismo y vistas panorámicas.</li>
                <li><u>Acantilados:</u> Al este, ofrecen un espectáculo visual impresionante, especialmente al atardecer.</li>
            </ul>
        </li>
    </ul>
</section>