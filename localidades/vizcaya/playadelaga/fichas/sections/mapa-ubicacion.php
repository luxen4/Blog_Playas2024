    <h2>Cómo Llegar</h2>
    <p>La Playa de Laga es accesible tanto en coche como en transporte público, ofreciendo varias opciones para llegar al destino.</p>
    <ul>
        <li><strong>En coche:</strong> Desde Bilbao, la Playa de Laga se encuentra a unos 40 minutos en coche por la carretera BI-2113. Hay opciones de aparcamiento en las inmediaciones de la playa, incluyendo zonas gratuitas y de pago.</li>
        <li><strong>En autobús:</strong> La playa está conectada mediante autobuses desde Bilbao y otras localidades cercanas. Consulta los horarios y rutas de la empresa de autobuses Bizkaibus.</li>
        <li><strong>A pie o en bicicleta:</strong> Laga cuenta con rutas escénicas que facilitan el acceso a la playa caminando o en bicicleta, permitiendo disfrutar del entorno natural y las vistas costeras.</li>
    </ul>
</section>